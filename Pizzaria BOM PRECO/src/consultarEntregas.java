    private void preencher() {
        int linha = jtlD.getSelectedRow();
        if (linha != -1) {
            nomeCliente.setText((String) jtlD.getValueAt(linha, 0));
            cpf.setText((String) jtlD.getValueAt(linha, 1));
            end.setText((String) jtlD.getValueAt(linha, 2));
            telefone.setText((String) jtlD.getValueAt(linha, 3));
            pedido.setText((String) jtlD.getValueAt(linha, 4));
        } else {
            JOptionPane.showMessageDialog(this,
                    "Você não selecionou nenhuma linha!");
        }
    }
