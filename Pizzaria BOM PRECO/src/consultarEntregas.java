    private void confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarActionPerformed
        Funcoes nova = new Funcoes();
        DefaultTableModel dtm = new DefaultTableModel();
        nova.cadastrarHistorico(Integer.parseInt(pedido.getText()));
        nova.removerEntrega(pedido.getText());
        dtm.setNumRows(0);
        limparTabela();
        DefaultTableModel val = (DefaultTableModel) jtlD.getModel();
        val.setNumRows(0);
        
    }//GEN-LAST:event_confirmarActionPerformed

    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        Funcoes novo = new Funcoes();
        DefaultTableModel val = (DefaultTableModel) jtlD.getModel();
        String mov = novo.listaEntregas();
        String[] teste = mov.split(";");
        int tam = teste.length;
        for(int i=1;i<tam;i++){
            String[] divisao = teste[i].split(",");
            val.addRow(new String[]{divisao[0],divisao[1],divisao[2],divisao[3],divisao[4]});
            
        }
        
    }//GEN-LAST:event_jbSalvarActionPerformed

